export function t(key: string, lang: Language = 'hi'): string {
  const keys = key.split('.');
  let value: Record<string, unknown> = translations;
  
  for (const k of keys) {
    value = value[k] as Record<string, unknown>;
    if (!value) return key;
  }
  
  return (value[lang] as string) || (value['en'] as string) || key;
}

